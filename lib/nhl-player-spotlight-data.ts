export function getDailyNHLPlayer(date?: Date): NHLSpotlightPlayer {
  const now = date || new Date();
  const utc = Date.UTC(now.getFullYear(), now.getMonth(), now.getDate());
  const epochDay = Math.floor(utc / 86400000);
  const index = epochDay % nhlSpotlightPlayers.length;
  return nhlSpotlightPlayers[index];
}
